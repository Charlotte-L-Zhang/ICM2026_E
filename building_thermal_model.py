class BorealisOptimizedModel(BuildingPhysicsModel):
    def run_simulation(self, weather_df):
        sim_hours = len(weather_df)
        dt = 3600

        T_in_log = np.zeros(sim_hours)
        Q_heat_log = np.zeros(sim_hours)  # 芬兰关注供暖
        PCM_State_Log = np.zeros(sim_hours)  # 记录PCM是吸热还是放热

        T_current = 20.0  # 初始温度
        T_set_heat = 20.0  # 供暖设定点 (低于此温度开启供暖)

        # --- 芬兰特有的材料参数 ---
        # 1. PCM (相变材料) 参数
        # 假设使用基于生物基的 PCM，熔点 21度
        pcm_melting_point = 21.0
        pcm_latent_heat = 180000  # J/kg (潜热值，很高)
        pcm_mass_total = 50000  # kg (假设全楼铺设了50吨 PCM 墙板/地板)

        # 2. 基础热容 (混凝土 + 家具)
        c_base_total = self.C_total

        print(f"❄️ Running Borealis Simulation: PCM Melting Point = {pcm_melting_point}°C")

        for i in range(sim_hours):
            row = weather_df.iloc[i]
            T_amb = row['Temp_Ambient']
            I_global = row['Solar_Rad']
            Sun_Alt = row['Solar_Altitude']

            # --- 策略 A: 极夜捕光 (Solar Capture) ---
            # 逻辑：芬兰冬天太阳很低，不需要遮阳，完全接受辐射
            # 甚至通过高 SHGC (0.7) 的玻璃引入热量
            factors = self.calculate_solar_incidence(Sun_Alt, row['Solar_Azimuth'])

            # 计算太阳得热 (无遮阳板阻挡)
            # 在芬兰，我们要区分直射和散射。阴天多，散射光重要。
            # 这里简化模型：假设高透光玻璃让更多光进来加热 PCM
            Q_sol = I_global * self.mat['shgc'] * (
                    self.A_win_S * factors['S'] +
                    (self.A_win_others / 3) * (factors['N'] + factors['E'] + factors['W'])
            )

            # --- 策略 B: 超级保温 (Super Insulation) ---
            # 极低的 U 值导致 Q_loss 变小，这已经在 self.UA_total 中体现了 (由传入的 mat 决定)
            # 但我们需要加上极低的风渗透 (Airtightness)
            # 芬兰建筑气密性极好，ACH 极低 (0.1 ~ 0.3)
            current_ach = 0.2
            vent_conductance = 1.2 * self.volume * 1000 * current_ach / 3600
            UA_effective = self.UA_total + vent_conductance

            Q_loss = UA_effective * (T_current - T_amb)

            # 内部热源 (在寒冷气候下，人就是热源，很重要)
            hour = row.name.hour if hasattr(row.name, 'hour') else i % 24
            Q_int = 25000 if 8 <= hour <= 18 else 3000  # 稍微调高一点，因为有供暖设备余热等

            # --- 策略 C: PCM 动态热容 (The "Thermal Battery") ---
            # 核心数学逻辑：有效热容法 (Effective Heat Capacity)
            # 当温度接近熔点时，C 瞬间变大，模拟潜热吸收/释放

            temperature_gap = T_current - pcm_melting_point

            # 定义一个高斯函数或矩形函数来模拟相变区间 (例如 +/- 1.5度)
            if abs(temperature_gap) < 1.5:
                # 处于相变区：C_effective = C_base + (Latent_Heat_Mass / Delta_T_Range)
                # 这意味着温度每变化 1度，需要极大的能量
                c_pcm_effective = pcm_mass_total * pcm_latent_heat / 3.0
                current_C = c_base_total + c_pcm_effective
                PCM_State_Log[i] = 1  # 标记为“相变活跃期”
            else:
                current_C = c_base_total
                PCM_State_Log[i] = 0

            # 计算能量平衡
            Q_net = Q_sol + Q_int - Q_loss

            # 预测下一时刻温度
            T_next = T_current + (Q_net * dt / current_C)

            # --- HVAC 供暖逻辑 ---
            energy_heat = 0
            if T_next < T_set_heat:
                # 需要供暖维持 20度
                energy_heat = current_C * (T_set_heat - T_next) / dt
                T_next = T_set_heat

            # 更新状态
            T_current = T_next
            T_in_log[i] = T_current
            Q_heat_log[i] = energy_heat

        results = weather_df.copy()
        results['T_indoor'] = T_in_log
        results['Heating_Load_W'] = Q_heat_log
        results['PCM_Active'] = PCM_State_Log

        total_heat_kwh = Q_heat_log.sum() / 1000
        return results, 0, total_heat_kwh  # 返回 Heating Load


