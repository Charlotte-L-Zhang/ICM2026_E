# 2. 核心天文算法 (大气专业基本功)
def calculate_sun_position(latitude, day_of_year):
    # 将纬度转换为弧度
    phi = np.radians(latitude)

    # 计算赤纬角 delta (Cooper方程)
    delta = np.radians(23.45 * np.sin(2 * np.pi * (284 + day_of_year) / 365))

    # 生成24小时的时间序列 (步长0.1小时)
    hours = np.arange(0, 24, 0.1)

    # 计算时角 omega (正午为0，每小时15度)
    # 早上是负数，下午是正数
    omega = np.radians((hours - 12) * 15)

    # --- 核心公式：计算太阳高度角 alpha ---
    sin_alpha = np.sin(phi) * np.sin(delta) + np.cos(phi) * np.cos(delta) * np.cos(omega)
    alpha = np.degrees(np.arcsin(np.clip(sin_alpha, -1, 1)))  # 限制在-1到1之间防止报错

    # 只有白天有太阳 (高度角 > 0)
    alpha[alpha < 0] = 0

    return hours, alpha

# 3. 遮阳投影计算 (几何部分)
def calculate_shadow_length(altitude_degrees, overhang_len):
    # 阴影长度 S = L * tan(90 - alpha) 或者 S = L / tan(alpha)
    # 当太阳高度角极低时，阴影无穷长
    shadows = []
    for alt in altitude_degrees:
        if alt <= 0:
            shadows.append(0)  # 没太阳就没影
        elif alt < 5:
            shadows.append(10)  # 太阳太低，影子充满整个窗户(设个最大值)
        else:
            # 简单的三角函数：Shadow = Overhang / tan(Altitude)
            # 注意：这是针对正南向窗户的简化版。如果是侧向窗户，要结合方位角
            tan_a = np.tan(np.radians(alt))
            shadow_len = overhang_len / tan_a
            shadows.append(shadow_len)
    return np.array(shadows)

