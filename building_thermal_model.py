def feed_epw_to_model(file_path, latitude):
    print(f"Reading: {file_path}")
    raw_data = pd.read_csv(file_path, skiprows=8, header=None)
    df = raw_data[[0, 1, 2, 3, 6, 13]].copy()
    df.columns = ['Year', 'Month', 'Day', 'Hour', 'Temp_Ambient', 'Solar_Rad']
    df['timestamp'] = pd.to_datetime(df[['Year', 'Month', 'Day']].assign(Hour=df['Hour'] - 1))
    df.set_index('timestamp', inplace=True)

    print(f"☀️ 正在为纬度 {latitude} 计算全年 8760 小时的太阳位置...")
    # --- 这里的循环是模型不崩溃的关键 ---
    altitudes, azimuths = [], []
    phi = np.radians(latitude)
    for idx, row in df.iterrows():
        doy = idx.timetuple().tm_yday
        omega = np.radians((idx.hour - 12) * 15)
        delta = np.radians(23.45 * np.sin(2 * np.pi * (284 + doy) / 365))

        # 算高度角
        sin_alpha = np.sin(phi) * np.sin(delta) + np.cos(phi) * np.cos(delta) * np.cos(omega)
        alpha_rad = np.arcsin(np.clip(sin_alpha, -1, 1))

        # 算方位角
        cos_azi = (np.sin(delta) - np.sin(alpha_rad) * np.sin(phi)) / (np.cos(alpha_rad) * np.cos(phi))
        azi_deg = np.degrees(np.arccos(np.clip(cos_azi, -1, 1)))
        if idx.hour > 12: azi_deg = 360 - azi_deg

        altitudes.append(max(0, np.degrees(alpha_rad)))
        azimuths.append(azi_deg)

    df['Solar_Altitude'] = altitudes
    df['Solar_Azimuth'] = azimuths
    return df


