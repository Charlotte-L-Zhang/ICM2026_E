# --- A. 定义“光之晶体”专用模型类 (含倾角几何 + PCM潜热) ---
class SolarPrismModel(BuildingPhysicsModel):
    def __init__(self, name, geometry, materials, location_lat, tilt_angle=60):
        # 继承父类的基础属性
        super().__init__(name, geometry, materials, location_lat)
        self.tilt_angle = tilt_angle  # 关键参数：南墙倾斜角度 (默认60度)

    def calculate_tilted_incidence(self, sun_alt, sun_azi):
        """
        [核心算法] 计算倾斜表面的太阳入射角
        公式：cos(θ) = cos(α)sin(β)cos(ψ-γ) + sin(α)cos(β)
        """
        # 1. 角度转弧度
        alt_r = np.radians(max(0, sun_alt))
        azi_r = np.radians(sun_azi)
        tilt_r = np.radians(self.tilt_angle)

        # 2. 设定墙面方位角 (南 = 180度)
        wall_azi_r = np.radians(180)

        # 3. 投影计算
        # 水平分量 (Horizontal Component)
        term1 = np.cos(alt_r) * np.sin(tilt_r) * np.cos(azi_r - wall_azi_r)
        # 垂直分量 (Vertical Component)
        term2 = np.sin(alt_r) * np.cos(tilt_r)

        # 结果截断 (负数表示背光，取0)
        return max(0, term1 + term2)

    def run_prism_simulation(self, weather_df):
        """
        [核心仿真] 加入了 '微时间步长' (Sub-stepping) 以精确模拟 PCM 相变
        """
        # 将步长从 1小时(3600s) 缩短到 10分钟(600s)
        dt = 600
        steps_per_hour = 3600 // dt

        sim_hours = len(weather_df)

        # 初始化记录数组
        T_in_log = np.zeros(sim_hours)
        Q_heat_log = np.zeros(sim_hours)
        PCM_State_Log = np.zeros(sim_hours)  # 1=吸热, -1=放热, 0=休眠

        # 初始状态
        T_current = 20.0
        T_set_heat = 20.0  # 供暖底线

        # --- PCM (德令哈盐胶囊) 参数 ---
        pcm_melting_point = 22.0  # 设定的相变温度 (舒适区中心)
        pcm_capacity = 60000 * 180000  # 60吨材料 * 潜热值 -> 总能量池 (J)
        pcm_storage = 0.0  # 当前储存的潜热 (J)

        print(f"💎 Running Solar Prism Simulation (Tilt={self.tilt_angle}°)...")

        for i in range(sim_hours):
            row = weather_df.iloc[i]
            T_amb = row['Temp_Ambient']
            I_global = row['Solar_Rad']

            # --- Step 1: 几何捕光计算 ---
            # 南墙 (倾斜) 使用特殊公式
            inc_S = self.calculate_tilted_incidence(row['Solar_Altitude'], row['Solar_Azimuth'])
            # 其他墙 (垂直) 使用普通公式
            factors_vert = self.calculate_solar_incidence(row['Solar_Altitude'], row['Solar_Azimuth'])

            # 总太阳得热 (South Tilted + Others Vertical)
            # 假设南墙因倾斜面积增加 (A_new = A_old / sin(tilt))，此处简化处理，直接用高SHGC体现
            Q_sol = I_global * self.mat['shgc'] * (
                    self.A_win_S * inc_S +
                    (self.A_win_others / 3) * (factors_vert['N'] + factors_vert['E'] + factors_vert['W'])
            )

            # --- Step 2: 小时内的精细循环 (Sub-loop) ---
            hourly_heat_sum = 0

            for step in range(steps_per_hour):
                # 2.1 热损耗 (Q_loss)
                # 极地模式：气密性极高 (ACH=0.1)
                vent_loss = 1.2 * self.volume * 1000 * 0.1 / 3600 * (T_current - T_amb)
                cond_loss = self.UA_total * (T_current - T_amb)
                Q_loss = vent_loss + cond_loss

                # 2.2 内部得热 (Q_int) - 白天人多热多
                hour = row.name.hour if hasattr(row.name, 'hour') else i % 24
                Q_int = 25000 if 8 <= hour <= 18 else 3000

                # 2.3 净热流
                Q_net = Q_sol + Q_int - Q_loss

                # 2.4 PCM 核心逻辑：温度锁定
                dT_potential = Q_net * dt / self.C_total
                T_potential = T_current + dT_potential

                # 判断是否在相变区间 (21°C - 23°C)
                if 21.0 < T_current < 23.0:
                    if Q_net > 0 and pcm_storage < pcm_capacity:
                        # 有余热 -> 存入PCM，温度不升
                        pcm_storage += Q_net * dt
                        T_next = T_current
                        PCM_State_Log[i] = 1
                    elif Q_net < 0 and pcm_storage > 0:
                        # 缺热 -> PCM释放，温度不降
                        pcm_storage += Q_net * dt  # Q_net是负值
                        T_next = T_current
                        PCM_State_Log[i] = -1
                    else:
                        # 存满或放空了 -> 温度变化
                        T_next = T_potential
                else:
                    T_next = T_potential

                # 2.5 辅助供暖 (Heating System)
                step_heat = 0
                if T_next < T_set_heat:
                    step_heat = self.C_total * (T_set_heat - T_next) / dt
                    T_next = T_set_heat  # 强制拉回20度

                hourly_heat_sum += step_heat
                T_current = T_next

            # --- Step 3: 记录该小时数据 ---
            T_in_log[i] = T_current
            Q_heat_log[i] = hourly_heat_sum / steps_per_hour  # 存平均功率 (W)

        # 整理输出
        results = weather_df.copy()
        results['T_indoor'] = T_in_log
        results['Heating_Load_W'] = Q_heat_log
        results['PCM_State'] = PCM_State_Log

        total_heat_kwh = Q_heat_log.sum() / 1000
        return results, total_heat_kwh


