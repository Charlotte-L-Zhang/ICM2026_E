class BuildingPhysicsModel:
    # --- 替换 __init__ 中的几何部分 ---
    def __init__(self, name, geometry, materials, location_lat):
        self.name = name
        self.geo = geometry
        self.mat = materials
        self.lat = location_lat

        # 1. 严格按照题目：60m x 24m, 两层
        self.L, self.W = 60.0, 24.0
        self.H_total = 7.0  # 两层总高
        self.area_floor = self.L * self.W
        self.volume = self.area_floor * self.H_total

        # 2. 计算各向墙面积 (Gross Area)
        gross_S = self.L * self.H_total
        gross_N = self.L * self.H_total
        gross_EW = self.W * self.H_total * 2  # 东西两面墙总和

        # 3. 严格按照题目 WWR：南向45%，其他30%
        self.A_win_S = gross_S * 0.45
        self.A_win_others = (gross_N + gross_EW) * 0.30

        # 总窗户面积
        self.A_win_total = self.A_win_S + self.A_win_others
        # 实墙面积 (砖贴面)
        self.A_wall_opaque = (gross_S + gross_N + gross_EW) - self.A_win_total
        self.A_roof = self.area_floor

        # 4. 重新计算总热阻 UA
        self.UA_total = (self.A_wall_opaque * materials['u_wall']) + \
                        (self.A_win_total * materials['u_window']) + \
                        (self.A_roof * materials['u_roof'])

        # 5. 计算热质量 C (此处对应学姐图中的 Q_stored = m*c*dT)
        self.C_total = self.area_floor * materials['thermal_mass_per_area']

    def calculate_solar_incidence(self, sun_alt, sun_azi):
        """
        计算垂直墙面上的入射系数 (Cosine Factor)
        简单的几何投影：cos(theta) = cos(alt) * cos(azi - wall_azi)
        """
        # 转换为弧度
        alt_rad = np.radians(max(0, sun_alt))  # 只有白天有太阳
        azi_rad = np.radians(sun_azi)

        # 定义墙面法线方位角 (南=180, 北=0, 东=90, 西=270 - 视具体坐标系而定)
        # 假设通用气象坐标系: 北=0/360, 东=90, 南=180, 西=270

        # 南墙 (Facing 180)
        inc_S = np.cos(alt_rad) * np.cos(azi_rad - np.radians(180))
        # 北墙 (Facing 0)
        inc_N = np.cos(alt_rad) * np.cos(azi_rad - np.radians(0))
        # 东墙 (Facing 90)
        inc_E = np.cos(alt_rad) * np.cos(azi_rad - np.radians(90))
        # 西墙 (Facing 270)
        inc_W = np.cos(alt_rad) * np.cos(azi_rad - np.radians(270))

        return {
            'S': max(0, inc_S),
            'N': max(0, inc_N),
            'E': max(0, inc_E),
            'W': max(0, inc_W)
        }

    def run_simulation(self, weather_df):
        """
        核心物理循环
        weather_df 必须包含: 'Temp_Ambient', 'Solar_Rad' (Global), 以及计算好的 'Solar_Altitude', 'Solar_Azimuth'
        """
        sim_hours = len(weather_df)
        dt = 3600  # 时间步长 (秒)

        # 初始化结果数组
        T_in_log = np.zeros(sim_hours)
        Q_cool_log = np.zeros(sim_hours)
        Q_heat_log = np.zeros(sim_hours)

        # 初始温度
        T_current = 22.0

        # 舒适度设定 (死区 Deadband)
        T_set_cool = 26.0
        T_set_heat = 20.0

        print(f"Running Simulation: {self.name}...")

        for i in range(sim_hours):
            # 1. 获取当前环境参数
            row = weather_df.iloc[i]
            T_amb = row['Temp_Ambient']
            I_global = row['Solar_Rad']  # 水平面总辐射

            # 2. 计算太阳得热 (Projected Solar Gain)
            # 垂直面辐射估算：简单假设 I_vert = I_global * Incidence_Factor
            # (更复杂的模型会分离直射/散射，这里做基准足够了)
            factors = self.calculate_solar_incidence(row['Solar_Altitude'], row['Solar_Azimuth'])

            # --- 修复后的 Q_sol 计算逻辑 ---
            factors = self.calculate_solar_incidence(row['Solar_Altitude'], row['Solar_Azimuth'])

            # 引入被动遮阳系数 (Shading Factor)，默认1.0(无遮阳)，优化后可设为0.4
            shading_factor = self.mat.get('shading_factor', 1.0)

            # 核心修正：使用 A_win_S 和 A_win_others 匹配题目给出的不对称窗墙比
            # 我们假设非南向的三个面平分 A_win_others 的面积
            A_per_other_side = self.A_win_others / 3

            Q_sol = I_global * self.mat['shgc'] * shading_factor * (
                    self.A_win_S * factors['S'] +
                    A_per_other_side * (factors['N'] + factors['E'] + factors['W'])
            )

            # 3. 计算其他热流
            # 传导 + 通风
            # Q = UA * dT + (rho*V*cp*ACH/3600) * dT
            vent_factor = 1.2 * self.volume * 1000 * 0.5 / 3600  # 假设 ACH=0.5, 空气密度1.2, 比热1000

            UA_effective = self.UA_total + vent_factor
            Q_loss = UA_effective * (T_current - T_amb)  # 正值代表热量流出

            # 内部热源 (简单假设白天有，晚上无)
            hour = row.name.hour if hasattr(row.name, 'hour') else i % 24
            Q_int = 20000 if 8 <= hour <= 18 else 2000  # 20kW (白天) / 2kW (晚上待机)

            # 4. 预测下一时刻温度 (无空调)
            # dT = (Q_gain - Q_loss) * dt / C
            Q_net = Q_sol + Q_int - Q_loss
            T_next = T_current + (Q_net * dt / self.C_total)

            # 5. HVAC 控制逻辑 (理想温控)
            energy_cool = 0
            energy_heat = 0

            if T_next > T_set_cool:
                # 需要制冷，把温度拉回 T_set_cool
                # Energy = C * (T_next - T_set)
                energy_cool = self.C_total * (T_next - T_set_cool) / dt  # W
                T_next = T_set_cool

            elif T_next < T_set_heat:
                # 需要供暖
                energy_heat = self.C_total * (T_set_heat - T_next) / dt  # W
                T_next = T_set_heat

            # 更新状态
            T_current = T_next

            # 记录
            T_in_log[i] = T_current
            Q_cool_log[i] = energy_cool
            Q_heat_log[i] = energy_heat

        # 汇总结果
        results = weather_df.copy()
        results['T_indoor'] = T_in_log
        results['Cooling_Load_W'] = Q_cool_log
        results['Heating_Load_W'] = Q_heat_log

        # 转换为 kWh
        total_cool_kwh = Q_cool_log.sum() / 1000
        total_heat_kwh = Q_heat_log.sum() / 1000

        print(f"Simulation Done.")
        print(f"Total Cooling: {total_cool_kwh:,.0f} kWh")
        print(f"Total Heating: {total_heat_kwh:,.0f} kWh")

        return results, total_cool_kwh, total_heat_kwh


